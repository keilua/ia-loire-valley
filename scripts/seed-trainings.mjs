// Supprimer toutes les formations existantes
console.log('🗑️  Suppression des formations existantes…')
const existing = await client.fetch('*[_type == "training"]._id')
if (existing.length > 0) {
  const tx = client.transaction()
  for (const id of existing) tx.delete(id)
  await tx.commit()
  console.log(`   ${existing.length} formation(s) supprimée(s)`)
} else {
  console.log('   Aucune formation existante')
}

// Insérer les nouvelles
console.log(`\n🚀 Insertion de ${trainings.length} formations…`)
for (const training of trainings) {
  const doc = await client.create(training)
  console.log(`✅ ${training.title.slice(0, 60)}… → ${doc._id}`)
}

console.log('\n🎉 Toutes les formations ont été ajoutées !')
